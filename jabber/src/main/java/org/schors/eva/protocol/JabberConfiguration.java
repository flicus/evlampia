package org.schors.eva.protocol;

import org.schors.eva.configuration.AbstractConfiguration;
import org.schors.eva.configuration.ConfigurationSection;

import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import java.util.ArrayList;
import java.util.List;

@XmlRootElement(name = "jabber")
@ConfigurationSection
public class JabberConfiguration extends AbstractConfiguration {
    @XmlElement(defaultValue = "jabber.ru")
    private String host;
    @XmlElement(defaultValue = "anon")
    private String jabberId;
    @XmlElement(defaultValue = "god")
    private String password;
    @XmlElement(defaultValue = "Евлампия")
    private String nick;
    @XmlElement(defaultValue = "Евлампия")
    private String firstName;
    @XmlElement(defaultValue = "Шандараховна")
    private String lastName;
    @XmlElement(defaultValue = "foo@fee.bar")
    private String email;
    @XmlElement(defaultValue = "Совхоз \"Светлый Путь Слоупока\"")
    private String organization;
    private List<String> rooms = new ArrayList<>();

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getJabberId() {
        return jabberId;
    }

    public void setJabberId(String jabberId) {
        this.jabberId = jabberId;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getNick() {
        return nick;
    }

    public void setNick(String nick) {
        this.nick = nick;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getOrganization() {
        return organization;
    }

    public void setOrganization(String organization) {
        this.organization = organization;
    }

    public List<String> getRooms() {
        return rooms;
    }

    public void setRooms(List<String> rooms) {
        this.rooms = rooms;
    }
}
